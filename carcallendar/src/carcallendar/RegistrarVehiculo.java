    private void cargarVehiculos() {
        try {
        Connection con = dbconection.conectar();
        String query = "SELECT * FROM vehiculo WHERE idusuario = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setInt(1, usuarioId);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTableVehiculos.getModel();
        model.setRowCount(0);  // Limpia la tabla

        while (rs.next()) {
            model.addRow(new Object[]{rs.getString("placa"), rs.getString("marca"), rs.getString("modelo")});
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al cargar vehículos: " + e.getMessage());
    }
    }
