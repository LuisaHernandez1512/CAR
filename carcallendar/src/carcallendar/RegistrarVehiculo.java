    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String placa = txtPlaca.getText();
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();

        try {
            Connection con = dbconection.conectar();
            String query = "INSERT INTO vehiculo (placa, marca, modelo, idusuario) VALUES (?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, placa);
            pst.setString(2, marca);
            pst.setString(3, modelo);
            pst.setInt(4, usuarioId);  
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Vehículo registrado con éxito");
            cargarVehiculos();  
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al registrar vehículo: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

